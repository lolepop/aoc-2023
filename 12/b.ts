const parseInput = (input: string): [string[], number[]][] =>
    input.split("\n").map(line => {
        const [puzzle, seq] = line.split(" ")
        return [puzzle.split(""), seq.split(",").map(n => parseInt(n))]
    })

const expand = (puzzle: string[], seq: number[]): [string[], number[]] => [
    [...new Array(5).fill(puzzle)].flatMap(puzzle => [...puzzle, "?"]).slice(0, -1),
    new Array(5).fill(seq).flat()
]

const solvePuzzle = (puzzle: string[], seq: number[]) => {
    const mem: { [k: string]: number } = {}
    const solve = (pi: number, si: number, matches: number): number => {
        const k = [pi, si, matches].join(" ")
        if (k in mem) return mem[k]

        if (pi >= puzzle.length) {
            // console.log(pi, si, matches);
            // reject if does not fulfill all contig checks 
            // first condition ends with .
            // second condition ends with #
            if ((si >= seq.length && matches === 0) || (si === seq.length - 1 && seq[si] === matches))
                return 1
            return 0
        }

        const possibleTiles = puzzle[pi] === "?" ? ["#", "."] : [puzzle[pi]]
        const acc = possibleTiles.map(tile => {
            if (tile === "#") {
                return solve(pi + 1, si, matches + 1)
            } else {
                // only accept if fully matches sequence. reject if partial match
                if (matches > 0) {
                    if (seq[si] === matches)
                        return solve(pi + 1, si + 1, 0)
                    return 0
                }
                return solve(pi + 1, si, 0)
            }
        }).reduce((acc, v) => acc + v)
        mem[k] = acc
        return acc
    }

    const res = solve(0, 0, 0)
    // console.log(res);
    return res
}

